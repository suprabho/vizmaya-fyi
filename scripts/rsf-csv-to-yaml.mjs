const SKIP = new Set(["CSS", "CTU"])

function parseNum(raw) {
  if (raw === undefined || raw === "") return null
  const cleaned = String(raw).replace(/,/g, ".")
  const n = Number(cleaned)
  return Number.isFinite(n) ? n : null
}

function readCsv(path) {
  const raw = readFileSync(path, "latin1")
  const lines = raw.split(/\r?\n/).filter((l) => l.trim().length > 0)
  const header = lines[0].split(";").map((s) => s.trim())
  return lines.slice(1).map((line) => {
    const cells = line.split(";")
    const row = {}
    header.forEach((col, i) => { row[col] = cells[i] !== undefined ? cells[i].trim() : "" })
    return row
  })
}

const csvPath = process.argv[2] || resolve(process.cwd(), "../../../vizmaya-data/2026.csv")
const valueColumn = process.argv[3] || "Score 2026"
const rows = readCsv(csvPath)

const items = []
const skipped = []
for (const row of rows) {
  const iso3 = row.ISO
  if (!iso3) continue
  if (SKIP.has(iso3)) { skipped.push(`${iso3} (skipped: not a country)`); continue }
  const iso2 = ISO3_TO_ISO2[iso3]
  if (!iso2) { skipped.push(`${iso3} (no ISO2 mapping)`); continue }
  const value = parseNum(row[valueColumn])
  if (value === null) { skipped.push(`${iso3} (missing value)`); continue }
  items.push({ code: iso2, value, name: row.Country_EN, rank: parseNum(row.Rank) })
}

// Sort by rank ascending (rank 1 = best/highest score)
items.sort((a, b) => (a.rank ?? 9999) - (b.rank ?? 9999))

const lines = []
lines.push(`# Generated by scripts/rsf-csv-to-yaml.mjs from RSF 2026.csv (${items.length} entries)`)
lines.push(`# Source: https://rsf.org/en/index — column: ${valueColumn}`)
lines.push("items:")
for (const it of items) {
  const safeName = (it.name || "").replace(/"/g, '\\"').trim()
  lines.push(`  - code: "${it.code}"   # ${safeName} (rank ${it.rank})`)
  lines.push(`    value: ${it.value}`)
}
console.log(lines.join("\n"))

if (skipped.length) {
  console.error(`\n# ${skipped.length} rows skipped:`)
  for (const s of skipped) console.error(`#   ${s}`)
}
