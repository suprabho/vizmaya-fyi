// Usage:
//   node scripts/rsf-csv-to-yaml.mjs > /tmp/rsf-items.yaml
//
// CSV path is hardcoded to vizmaya-data/2026.csv (sibling repo). Override with
// the first CLI arg. Values for the global choropleth are the overall score
